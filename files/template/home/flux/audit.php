<p>Dans ce contexte, K'nGELL apporte à ces clients des outils leur permettant d'ajuster leurs flux à court et à moyen
    terme. l'un de ces outil est l'audit 360°. Selon les besoins, il peutêtre
    global ou ciblé sur un processus, une opérations ou une activité.</p>
<h3>Principes de l'audit</h3>
<p>l'audit 360° consiste, sur la base d'un référentiel logistique/Supply, à auditer tout ou partie du système
    logistique, depuis la stratégie, jusqu'aux opérations ciblées. La durée de l'audit peut varier entre 1 et 3 jours
    selon
    le périmètre. Puis, un rapport d'audit contenant une feuille de route
    est remis à l'entreprise</p>